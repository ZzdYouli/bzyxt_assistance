def adventure_switch(adventure_name):
    if adventure_name == "赌场":
        return "casino"
    elif adventure_name == "家":
        return "home"
    elif adventure_name == "残破的小镇":
        return "broken_town"
    elif adventure_name == "竹林":
        return "bamboo"
    elif adventure_name == "小池塘":
        return "pool"
    else:

        return adventure_name
